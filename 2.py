class Animal:
    def move(self):
        print("The animal moves in its own way.")

class Dog(Animal):
    def move(self):
        print("The dog runs 🐕.")

class Bird(Animal):
    def move(self):
        print("The bird flies 🐦.")

class Vehicle:
    def move(self):
        print("The vehicle moves in its own way.")

class Car(Vehicle):
    def move(self):
        print("The car drives 🚗.")

class Plane(Vehicle):
    def move(self):
        print("The plane flies ✈️.")

